package Graphs;

import Data_Structures.Bag;


public class Graph {

    private Bag<Integer>[] adj; //Listy sąsiedstwa
    private final int V; ///number of nodes
    private int E; // liczba krawędzi

    public Graph(int V) {
        this.V = V;
        this.E = 0;
        adj = (Bag<Integer>[]) new Bag[V];
        for (int v = 0; v < V; v++) {
            adj[v] = new Bag<Integer>();
        }
    }

    public Graph(In in) {
        this(in.readInt());
        int E = in.readInt();
        for (int i = 0; i < E; i++) {
            int v = in.readInt();
            int w = in.readInt();
            addEdge(v, w);

        }

    }

    private void addEdge(int v, int w) {
        adj[v].add(w);
        adj[w].add(v);
        E++;
    }

    public Iterable<Integer> adj(int v) {
        return adj[v];
    }

    public int V(){return this.V;}
    public int E(){return this.E;}


}
